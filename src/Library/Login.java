     public boolean checkLogin( String email, String password) {

        boolean isValid = false;
        PreparedStatement pst = null;
        ResultSet rs = null;
        try (Connection con = databaseConnection.getConnection()) {
            pst = con.prepareStatement("SELECT * FROM registration WHERE email = ? AND pass = ?");
            pst.setString(1, email);
            pst.setString(2, password);

            rs = pst.executeQuery();

            if (rs.next()) {
                isValid = true;
                System.out.println("Login successful!");
            }
        } catch (SQLException ex) {
            System.err.println("Error during login validation: " + ex.getMessage());
        } finally {
            try {
                if (rs != null) {
                    rs.close();
                }
            } catch (SQLException ex) {
                System.err.println("Error closing ResultSet: " + ex.getMessage());
            }
            try {
                if (pst != null) {
                    pst.close();
               }
            } catch (SQLException ex) {
                System.err.println("Error closing PreparedStatement: " + ex.getMessage());
            }
        }

        return isValid;
    }
